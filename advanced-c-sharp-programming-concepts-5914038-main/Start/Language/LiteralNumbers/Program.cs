// Numbers can be made more readable using _ as a number separator
int d = 123456;
float f = 1234.5f;
var x = 0xABCDEF;
var b = 0b1101111010010010;

Console.WriteLine($"{d}");
Console.WriteLine($"{f}");
Console.WriteLine($"{b:X}");
Console.WriteLine($"{x:X}");
